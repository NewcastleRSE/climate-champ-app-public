export default function convertQuestionNumber(number) {
  switch (number) {
    case 1:
      return 'ones'
    case 2:
      return 'twos'
    case 3:
      return 'threes'
    case 4:
      return 'fours'
    case 5:
      return 'fives'
    case 6:
      return 'sixes'
    case 7:
      return 'sevens'
    case 8:
      return 'eights'
    case 9:
      return 'nines'
    case 10:
      return 'tens'
    case 11:
      return 'elevens'
    case 12:
      return 'twelves'
  }
}
